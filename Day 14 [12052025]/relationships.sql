CREATE TABLE CATEGORY(
	CATEGORY_ID INT PRIMARY KEY,
    CATEGORY_NAME VARCHAR(20) NOT NULL
);
INSERT INTO CATEGORY VALUES (1,"C1"),(2,"C2");
